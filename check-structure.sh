OUTPUT="project-structure-report.md"

# ฟังก์ชันช่วยจัดรูปแบบ
function print_tree {
  local dir=$1
  local prefix=$2
  local max_depth=3

  # ใช้ find แสดงไฟล์และโฟลเดอร์ลึก 3 ชั้น
  find "$dir" -maxdepth $max_depth | sed -e "s|^$dir||" | sed 's|[^/]*/|  |g' | grep -v '^$' 
}

echo "# Project Directory Structure Report" > "$OUTPUT"
echo "" >> "$OUTPUT"
echo "Generated on $(date)" >> "$OUTPUT"
echo "" >> "$OUTPUT"
echo "## Directory Tree (up to 3 levels deep)" >> "$OUTPUT"
echo '```' >> "$OUTPUT"

# กำหนด path โฟลเดอร์โปรเจค (แก้ถ้าต้องการ)
PROJECT_ROOT="."

# สั่งพิมพ์โครงสร้างโฟลเดอร์ ลึก 3 ชั้น พร้อมจัด format
tree -L 3 "$PROJECT_ROOT" >> "$OUTPUT" 2>/dev/null || print_tree "$PROJECT_ROOT" "" >> "$OUTPUT"

echo '```' >> "$OUTPUT"
echo "" >> "$OUTPUT"

echo "## Summary" >> "$OUTPUT"
echo "- Total directories: $(find "$PROJECT_ROOT" -type d | wc -l)" >> "$OUTPUT"
echo "- Total files: $(find "$PROJECT_ROOT" -type f | wc -l)" >> "$OUTPUT"
echo "" >> "$OUTPUT"

echo "## Top-level files:" >> "$OUTPUT"
find "$PROJECT_ROOT" -maxdepth 1 -type f -exec basename {} \; | sort >> "$OUTPUT"

echo "" >> "$OUTPUT"
echo "## Top-level directories:" >> "$OUTPUT"
find "$PROJECT_ROOT" -maxdepth 1 -mindepth 1 -type d -exec basename {} \; | sort >> "$OUTPUT"

